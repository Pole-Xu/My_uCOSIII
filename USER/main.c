#define START_TASK_PRIO		3                   //任务优先级
#define START_STK_SIZE 		512                 //任务堆栈大小
OS_TCB StartTaskTCB;                            //任务控制块
CPU_STK START_TASK_STK[START_STK_SIZE];         //任务堆栈
void start_task(void *p_arg);                   //任务函数


#define HIGH_TASK_PRIO		4
#define HIGH_STK_SIZE 		128
OS_TCB HighTaskTCB;
CPU_STK HIGH_TASK_STK[HIGH_STK_SIZE];
void high_task(void *p_arg);

#define LOW_TASK_PRIO		5
#define LOW_STK_SIZE 		128
OS_TCB LowTaskTCB;
CPU_STK LOW_TASK_STK[LOW_STK_SIZE];
void low_task(void *p_arg);

#define LED_TASK_PRIO		6
#define LED_STK_SIZE 		128
OS_TCB LedTaskTCB;
CPU_STK LED_TASK_STK[LED_STK_SIZE];
void led_task(void *p_arg);


OS_SEM gSEM;
OS_TMR gTMR;

void usr_my_init(void){
    OS_ERR err;
    
    delay_init();       // 延时初始化
	NVIC_PriorityGroupConfig(NVIC_PriorityGroup_2); // 中断分组配置
	uart_init(115200);    // 串口波特率设置
	LED_Init();         // LED初始化
	Usr_Gpio_Init();    // Usr Init
	OSInit(&err);		// 初始化UCOSIII
}

int main(void)
{
	OS_ERR err;
	CPU_SR_ALLOC();
	
    usr_my_init();   // usr init 
	
	OS_CRITICAL_ENTER();//进入临界区
	//创建开始任务
	OSTaskCreate((OS_TCB 	* )&StartTaskTCB,		//任务控制块
				 (CPU_CHAR	* )"start task", 		//任务名字
                 (OS_TASK_PTR )start_task, 			//任务函数
                 (void		* )0,					//传递给任务函数的参数
                 (OS_PRIO	  )START_TASK_PRIO,     //任务优先级
                 (CPU_STK   * )&START_TASK_STK[0],	//任务堆栈基地址
                 (CPU_STK_SIZE)START_STK_SIZE/10,	//任务堆栈深度限位
                 (CPU_STK_SIZE)START_STK_SIZE,		//任务堆栈大小
                 (OS_MSG_QTY  )0,					//任务内部消息队列能够接收的最大消息数目,为0时禁止接收消息
                 (OS_TICK	  )0,					//当使能时间片轮转时的时间片长度，为0时为默认长度，
                 (void   	* )0,					//用户补充的存储区
                 (OS_OPT      )OS_OPT_TASK_STK_CHK|OS_OPT_TASK_STK_CLR, //任务选项
                 (OS_ERR 	* )&err);				//存放该函数错误时的返回值
	OS_CRITICAL_EXIT();	//退出临界区	 
	OSStart(&err);  //开启UCOSIII
	while(1);
}

// Tmr CallBack
void gTMR_callback(void *p_tmr, void *p_arg)
{
	static int cnt=0;
	cnt++;		//定时器1执行次数加1
	printf("10s to go! cnt = %d.\r\n",cnt);
}

// Start Func
void start_task(void *p_arg)
{
	OS_ERR err;
	CPU_SR_ALLOC();
	p_arg = p_arg;

	CPU_Init();
#if OS_CFG_STAT_TASK_EN > 0u
   OSStatTaskCPUUsageInit(&err);  	//统计任务                
#endif
	
#ifdef CPU_CFG_INT_DIS_MEAS_EN		//如果使能了测量中断关闭时间
    CPU_IntDisMeasMaxCurReset();	
#endif
	
#if	OS_CFG_SCHED_ROUND_ROBIN_EN  //当使用时间片轮转的时候
	//使能时间片轮转调度功能,时间片长度为1个系统时钟节拍，既1*5=5ms
	OSSchedRoundRobinCfg(DEF_ENABLED,1,&err);  
#endif		

#if OS_CFG_APP_HOOKS_EN				//使用钩子函数
	//App_OS_SetAllHooks();			
#endif

   //创建信号量
    OSSemCreate ((OS_SEM   *)&gSEM,
                 (CPU_CHAR *)"gSEM",
                 (OS_SEM_CTR)0,
                 (OS_ERR   *)&err);
                 
	//创建定时器1
	OSTmrCreate((OS_TMR		*)&gTMR,		//定时器1
                (CPU_CHAR	*)"TMR",		//定时器名字
                (OS_TICK	 )0,			
                (OS_TICK	 )1000,          //100*10=1000ms
                (OS_OPT		 )OS_OPT_TMR_PERIODIC, //周期模式
                (OS_TMR_CALLBACK_PTR)gTMR_callback,//回调函数
                (void	    *)0,			//参数为0
                (OS_ERR	    *)&err);		//返回的错误码

	OS_CRITICAL_ENTER();	//进入临界区
	
	//创建LED任务
	OSTaskCreate((OS_TCB 	* )&LedTaskTCB,		
				 (CPU_CHAR	* )"Led Task", 		
                 (OS_TASK_PTR )led_task, 			
                 (void		* )0,					
                 (OS_PRIO	  )LED_TASK_PRIO,     
                 (CPU_STK   * )&LED_TASK_STK[0],	
                 (CPU_STK_SIZE)LED_STK_SIZE/10,	
                 (CPU_STK_SIZE)LED_STK_SIZE,		
                 (OS_MSG_QTY  )0,					
                 (OS_TICK	  )0,					
                 (void   	* )0,					
                 (OS_OPT      )OS_OPT_TASK_STK_CHK|OS_OPT_TASK_STK_CLR,
                 (OS_ERR 	* )&err);				
				 
	//创建High任务
	OSTaskCreate((OS_TCB 	* )&HighTaskTCB,		
				 (CPU_CHAR	* )"High Task", 		
                 (OS_TASK_PTR )high_task, 			
                 (void		* )0,					
                 (OS_PRIO	  )HIGH_TASK_PRIO,     	
                 (CPU_STK   * )&HIGH_TASK_STK[0],	
                 (CPU_STK_SIZE)HIGH_STK_SIZE/10,	
                 (CPU_STK_SIZE)HIGH_STK_SIZE,		
                 (OS_MSG_QTY  )0,					
                 (OS_TICK	  )0,					
                 (void   	* )0,				
                 (OS_OPT      )OS_OPT_TASK_STK_CHK|OS_OPT_TASK_STK_CLR, 
                 (OS_ERR 	* )&err);	

    //创建Low任务
	OSTaskCreate((OS_TCB 	* )&LowTaskTCB,		
				 (CPU_CHAR	* )"Low Task", 		
                 (OS_TASK_PTR )low_task, 			
                 (void		* )0,					
                 (OS_PRIO	  )LOW_TASK_PRIO,     	
                 (CPU_STK   * )&LOW_TASK_STK[0],	
                 (CPU_STK_SIZE)LOW_STK_SIZE/10,	
                 (CPU_STK_SIZE)LOW_STK_SIZE,		
                 (OS_MSG_QTY  )0,					
                 (OS_TICK	  )0,					
                 (void   	* )0,				
                 (OS_OPT      )OS_OPT_TASK_STK_CHK|OS_OPT_TASK_STK_CLR, 
                 (OS_ERR 	* )&err);
			 								 
	OS_TaskSuspend((OS_TCB*)&StartTaskTCB,&err);		//挂起开始任务			 
	OS_CRITICAL_EXIT();	//进入临界区
}


// LED Func
void led_task(void *p_arg)
{
	OS_ERR err;
	p_arg = p_arg;

	while(1)
	{   
	    printf("This is LED TASK!\r\n");
        LED0 = ~LED0;
		OSTimeDlyHMSM(0,0,1,0,OS_OPT_TIME_HMSM_STRICT,&err); //延时1s
	}
}

